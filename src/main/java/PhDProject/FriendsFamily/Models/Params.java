package PhDProject.FriendsFamily.Models;

import java.util.ArrayList;

public class Params {

	private String user;
	private ArrayList params;
	private String optimalError;
	
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public ArrayList getParams() {
		return params;
	}
	public void setParams(ArrayList params) {
		this.params = params;
	}
	public String getOptimalError() {
		return optimalError;
	}
	public void setOptimalError(String optimalError) {
		this.optimalError = optimalError;
	}
	
	
}
